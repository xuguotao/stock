import pandas as pd

from scripts.daily_fund_tail_advice import build_markdown_report


def test_build_markdown_report_summarizes_actions_in_chinese():
    report = pd.DataFrame(
        [
            {
                "基金名称": "华夏中证500指数增强C",
                "基金代码": "007995",
                "最新日期": "2026-06-11",
                "今日代理涨跌率": -0.0108,
                "技术信号": "回避",
                "信号原因": "弱于趋势",
                "操作等级": "C",
                "最终操作建议": "等待企稳",
                "建议原因": "连续弱势需要确认",
                "同类样本数": 29,
                "同类次日上涨概率": 0.5862,
                "同类次日下跌概率": 0.4138,
                "同类次日平均收益": -0.0007,
                "同类次日中位数收益": 0.0026,
                "同类次日跌超1%概率": 0.1724,
                "同类次日跌超2%概率": 0.1034,
            },
            {
                "基金名称": "易方达蓝筹精选混合",
                "基金代码": "005827",
                "最新日期": "2026-06-11",
                "今日代理涨跌率": -0.0107,
                "技术信号": "回避",
                "信号原因": "弱于趋势",
                "操作等级": "D",
                "最终操作建议": "不加仓",
                "建议原因": "同类历史下跌概率更高",
                "同类样本数": 22,
                "同类次日上涨概率": 0.3636,
                "同类次日下跌概率": 0.6364,
                "同类次日平均收益": -0.0024,
                "同类次日中位数收益": -0.0056,
                "同类次日跌超1%概率": 0.2727,
                "同类次日跌超2%概率": 0.0,
            },
        ]
    )

    markdown = build_markdown_report(report, trade_date="2026-06-11")

    assert "# 基金尾盘操作建议 - 2026-06-11" in markdown
    assert "总判断：今天不适合大额尾盘加仓" in markdown
    assert "| 华夏中证500指数增强C | 007995 | -1.08% | C | 等待企稳 |" in markdown
    assert "同类次日上涨/下跌概率：58.62% / 41.38%" in markdown
    assert "这不是保证收益的投资建议" in markdown
